/// Resolves an icon name string to an [IconData].
///
/// Returns [Icons.category_rounded] as fallback for unknown names.
IconData resolveCategoryIcon(String? iconName) {
  if (iconName == null || iconName.isEmpty) return Icons.category_rounded;
  return kCategoryIcons[iconName] ?? Icons.category_rounded;
}

