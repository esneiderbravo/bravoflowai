/// Returns the human-readable label for an icon key in the given language.
///
/// Falls back to English, then the raw key if no translation is available.
String resolveCategoryIconLabel(String key, String languageCode) {
  final t = kCategoryIconTranslations[key];
  if (t == null) return key;
  return t[languageCode] ?? t['en'] ?? key;
}
